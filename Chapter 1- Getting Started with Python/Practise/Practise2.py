#Create a Program to Output "Hi" 50 Times, Without any seperator 
for _ in range(50): #This loop is used to repeat a set of instructions a specific number of times, in this case, 50 times.
    print("Hi", end="") #"print" function used to print given word
                        #"end=""" is default set to /n which means newline but here we add "" which means nothing will be printed after "Hi"